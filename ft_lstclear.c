#include <stdlib.h>
#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list *p;

	p = *lst;
	while (p)
	{
		del(p->content);
		*lst = p->next;
		free(p);
		p = *lst;
	}
}
