#include "libft.h"

int	ft_atoi(char *s)
{
	int	i;
	int	sn;

	i = 0;
	sn = 1;
	while (ft_strchr(" \t\n\r\f\v", *s))
		++s;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			sn = -1;
		++s;
	}
	while (*s >= '0' && *s <= '9')
	{
		i = i * 10 + (*s - '0');
		++s;
	}
	return (sn * i);
}
