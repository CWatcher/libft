#include "libft.h"

int	ft_atoi(char *s)
{
	int	i;
	int	sn;

	if (!s || *s == '\0')
		return (0);
	i = 0;
	while (ft_strchr(" \t\n\r\f\v", *s))
		++s;
	sn = 1;
	if (*s == '-')
		sn = -1;
	s += (*s == '+' || *s == '-');
	while (*s >= '0' && *s <= '9')
	{
		i = i * 10 + (*s - '0');
		++s;
	}
	return (sn * i);
}
